package com.numberONE.maryfarm.Pick;

public class PickItem {
    int flowerImg;
    String flowerName;

    public PickItem(int flowerImg, String flowerName) {
        this.flowerImg = flowerImg;
        this.flowerName = flowerName;
    }

    public int getFlowerImg() {
        return flowerImg;
    }

    public String getFlowerName() {
        return flowerName;
    }
}
